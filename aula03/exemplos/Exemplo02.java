package  exemplos;

public class Exemplo02 {

    public static void main(String[] args) {
        //TIPO NOME
        int numero; // cria a  variavel chamado numero, que guarda um numero inteiro

        numero = 12; // (atribuição) guarda dentro da variavel

        System.out.println(numero); // mostra o que tem na variavel

        numero = 2 + 3 * 5; // substitui a conteudo da variavel pelo resultado da expresão

        System.out.println(numero);
    }
    
}
